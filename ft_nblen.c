#include "libft.h"

size_t			ft_nblen(int nbr)
{
	int		len;

	len = 0;
	if (nbr < 0)
		len++;
	while (nbr >= 10 || nbr <= -10)
	{
		nbr = nbr / 10;
		len++;
	}
	len++;
	return (len);
}
